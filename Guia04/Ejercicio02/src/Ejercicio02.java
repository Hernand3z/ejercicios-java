
/**
 * Ejercicio 2.
 *
 * Realiza un programa que muestre números múltiplos de 5 que comience en 0 y termine
 * en 100 usando el bucle While.
 *
 * @author Hernández
 */
public class Ejercicio02 {

    public static void main(String[] args) {

        int i = 0;

        while (i <= 100) {
            System.out.println(i);
            i = i + 5;
        }
    }
}
